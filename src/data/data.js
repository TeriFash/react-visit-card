const sortSkils = value => {
  const types = Object.keys(value);
  let list = [];

  types.forEach(item => {
    switch (item) {
      case 'hard':
        list.push({
          title: 'Technology',
          data: skills[item]
        });
        break;
      case 'soft':
        list.push({
          title: 'Tools',
          data: skills[item]
        });
        break;
      case 'management':
        list.push({
          title: 'Managment',
          data: skills[item]
        });
        break;
      default:
        list.push({
          title: 'Other',
          data: skills[item]
        });
        break;
    }
  });

  return list;
};

